namespace Scientific_Calculator
{
    public class Sci_calc
    {
        public int add(int a,int b)
        {
            return a + b;
        }
        public int sub(int a,int b)
        {
            return a - b;
        }
        public int mul(int a,int b)
        { 
            return a * b; 
        }
        public int div(int a, int b)
        {
            return a / b;
        }
        public string msg(string name)
        {
            return "Hello " + name;
        }
        
    }
   
}